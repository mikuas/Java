package com.project.demo;

public class dataType {
    public static void main(String[] args) {
        /*
        整数
         */
        // byte
        byte by_te = 11;
        // short
        short sho_rt = 45;
        // int
        int in_t = 14;
        // long
        // 如果定义long类型的变量
        // 在数值后面需要加一个l | L作为后缀
        long lo_ng = 1919810191981019198L;
        /*
        浮点数
         */
        // float
        float flo_at = 11.45F;
        System.out.println(lo_ng);
        // double
        double dou_ble = 1145.141919810;
        System.out.println(dou_ble);
        /*
        字符
         */
        // char
        char ch_ar = '你';
        /*
        布尔类型
         */
        boolean bool_ean = false;
        System.out.println(bool_ean);
    }
}
